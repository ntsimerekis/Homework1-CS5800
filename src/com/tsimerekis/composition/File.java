package com.tsimerekis.composition;

public class File {
	
	private String name;
	
	public File(final String name) {
		this.name = name;
	}
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	@Override
	public String toString() {
		return name;
	}
}
